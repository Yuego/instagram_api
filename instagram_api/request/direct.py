    def send_profile(self, recipients: list, user_id: int, options: dict = None): ...

    def send_reaction(self, thread_id: int, thread_item_id: int, reaction_type: str, options: dict = None): ...

    def send_story(self, recipients: list, story_id: int, reel_id: int = None, options: dict = None): ...

    def send_live(self, recipients: list, broadcast_id: int, options: dict = None): ...

    def delete_reaction(self, thread_id: int, thread_item_id: int, reaction_type: str, options: dict = None): ...

    def delete_item(self, thread_id: int, thread_item_id: int): ...

    def mark_item_seen(self, thread_id: int, thread_item_id: int): ...

    def mark_visual_items_seen(self, thread_id: int, thread_item_ids: List[int]): ...

    def mark_visual_items_replayed(self, thread_id: int, thread_item_ids: List[int]): ...


