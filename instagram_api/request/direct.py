    def send_text(self, recipients: list, text: str, options: dict = None): ...

    def send_post(self, recipients: list, media_id: int, options: dict = None): ...

    def send_photo(self, recipients: list, photo_filename: str, options: dict = None): ...

    def send_disappearing_photo(self, recipients: list, photo_filename: str, external_metadata: dict = None): ...

