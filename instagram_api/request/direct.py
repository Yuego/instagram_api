from typing import Optional, List

from .collection import Collection


class Direct(Collection):

    def get_inbox(self, cursor_id: Optional[str] = None): ...

    def get_pending_inbox(self, cursor_id: Optional[str] = None): ...

    def approve_pending_threads(self, threads: list): ...

